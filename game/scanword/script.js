function refreshBoardState() {
  const puzzle = state.sectionState?.puzzle;
  if (!puzzle) {
    return;
  }

  const activeWord = getWordById(state.activeWordId);
  const activeKeys = new Set(activeWord ? activeWord.cellKeys : []);

  elements.board.querySelectorAll(".letter-shell").forEach((shell) => {
    const key = shell.dataset.key;
    const input = shell.querySelector(".board-cell");
    const cell = getCellByKey(key);
    const value = state.sectionState.inputs[key] || "";
    const isCorrect = Boolean(value) && value === cell.char;
    const isRevealed = cell.wordIds.some((wordId) => state.sectionState.revealedWords.includes(wordId));

    if (input.value !== value) {
      input.value = value;
    }

    shell.classList.toggle("is-active", activeKeys.has(key));
    shell.classList.toggle("is-filled", Boolean(value));
    shell.classList.toggle("is-correct", isCorrect);
    shell.classList.toggle("is-wrong", state.wrongKeys.has(key));
    shell.classList.toggle("is-revealed", isRevealed);
  });

  elements.board.querySelectorAll(".clue-cell").forEach((button) => {
    const word = getWordById(button.dataset.wordId);
    const status = getWordStatus(word);
    button.classList.toggle("is-active", word.id === state.activeWordId);
    button.classList.toggle("is-solved", status.solved);
  });
}

