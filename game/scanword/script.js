async function loadData() {
  if (/^https?:$/i.test(window.location.protocol)) {
    try {
      const response = await fetch("./data/questions.json", { cache: "no-store" });
      if (response.ok) {
        return normalizeData(await response.json());
      }
    } catch (error) {
      console.warn("JSON fallback activated:", error);
    }
  }

  return normalizeData(FALLBACK_DATA);
}

function normalizeData(rawData) {
  const sections = Array.isArray(rawData.sections) ? rawData.sections : [];

  return {
    sections: sections
      .map((section, sectionIndex) => ({
        id: section.id || `section-${sectionIndex + 1}`,
        title: section.title || `Раздел ${sectionIndex + 1}`,
        badge: section.badge || `R${sectionIndex + 1}`,
        items: Array.isArray(section.items)
          ? section.items
              .map((item, itemIndex) => {
                const answer = String(item.answer || "").trim();
                const gridAnswer = sanitizeAnswer(item.gridAnswer || answer);

                return {
                  id: item.id || `${section.id || sectionIndex}-${itemIndex + 1}`,
                  question: String(item.question || "").trim(),
                  answer,
                  gridAnswer,
                  preview: makeCluePreview(String(item.question || "")),
                  alternatives: Array.isArray(item.alternatives) ? item.alternatives : [],
                };
              })
              .filter((item) => item.question && item.gridAnswer.length > 1)
          : [],
      }))
      .filter((section) => section.items.length),
  };
}

