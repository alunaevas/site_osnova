function playTone(kind) {
  const AudioContextClass = window.AudioContext || window.webkitAudioContext;
  if (!AudioContextClass) {
    return;
  }

  const presets = {
    success: [
      { frequency: 660, duration: 0.08, offset: 0, gain: 0.03 },
      { frequency: 880, duration: 0.12, offset: 0.08, gain: 0.028 },
    ],
    error: [
      { frequency: 220, duration: 0.12, offset: 0, gain: 0.03, type: "square" },
      { frequency: 180, duration: 0.15, offset: 0.08, gain: 0.028, type: "square" },
    ],
    accent: [
      { frequency: 520, duration: 0.08, offset: 0, gain: 0.03 },
      { frequency: 700, duration: 0.1, offset: 0.08, gain: 0.024 },
    ],
    complete: [
      { frequency: 523, duration: 0.09, offset: 0, gain: 0.03 },
      { frequency: 659, duration: 0.09, offset: 0.11, gain: 0.03 },
      { frequency: 784, duration: 0.14, offset: 0.23, gain: 0.032 },
    ],
  };

  const sequence = presets[kind];
  if (!sequence) {
    return;
  }

  if (!state.audioContext) {
    state.audioContext = new AudioContextClass();
  }

  const context = state.audioContext;
  if (context.state === "suspended") {
    context.resume().catch(() => {});
  }

  const now = context.currentTime;
  sequence.forEach((note) => {
    const oscillator = context.createOscillator();
    const gainNode = context.createGain();
    oscillator.type = note.type || "sine";
    oscillator.frequency.value = note.frequency;
    gainNode.gain.value = 0.0001;
    oscillator.connect(gainNode);
    gainNode.connect(context.destination);

    const start = now + note.offset;
    gainNode.gain.linearRampToValueAtTime(note.gain, start + 0.01);
    gainNode.gain.exponentialRampToValueAtTime(0.0001, start + note.duration);
    oscillator.start(start);
    oscillator.stop(start + note.duration + 0.02);
  });
}

function persistCurrentSection() {
  if (!state.currentSectionId) {
    return;
  }

  if (state.sectionState) {
    state.sessionSections[state.currentSectionId] = {
      puzzle: state.sectionState.puzzle,
      inputs: state.sectionState.inputs,
      revealedWords: state.sectionState.revealedWords,
      elapsedSeconds: state.sectionState.elapsedSeconds,
      completed: state.sectionState.completed,
      wordAudit: state.sectionState.wordAudit,
      activeWordId: state.sectionState.activeWordId,
      lastStatus: state.sectionState.lastStatus,
    };
  }

  state.storage.lastSectionId = state.currentSectionId;
  saveStorage();
}

function loadStorage() {
  try {
    const parsed = JSON.parse(window.localStorage.getItem(STORAGE_KEY));
    return parsed && typeof parsed === "object"
      ? { lastSectionId: parsed.lastSectionId || null }
      : { lastSectionId: null };
  } catch (error) {
    return { lastSectionId: null };
  }
}

function saveStorage() {
  try {
    window.localStorage.setItem(
      STORAGE_KEY,
      JSON.stringify({
        lastSectionId: state.storage.lastSectionId || null,
      })
    );
  } catch (error) {
    console.warn("Storage write skipped:", error);
  }
}

function findNearestPlayableCell(row, col, deltaRow, deltaCol) {
  const puzzle = state.sectionState?.puzzle;
  if (!puzzle) {
    return null;
  }

  let nextRow = row + deltaRow;
  let nextCol = col + deltaCol;

  while (isInsideGrid(nextRow, nextCol)) {
    const key = cellKey(nextRow, nextCol);
    if (puzzle.cellsByKey[key]) {
      return key;
    }
    nextRow += deltaRow;
    nextCol += deltaCol;
  }

  return null;
}

