function makeCluePreview(question) {
  const cleaned = question
    .replace(/[.,!?;:()"«»]/g, "")
    .replace(/\s+/g, " ")
    .trim();

  const parts = cleaned.split(" ").filter(Boolean);
  let preview = parts.slice(0, 2).join(" ");
  if (preview.length < 10 && parts[2]) {
    preview = `${preview} ${parts[2]}`;
  }
  if (preview.length > 18) {
    preview = `${preview.slice(0, 16).trim()}…`;
  }
  return preview || "Подсказка";
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function escapeSelectorValue(value) {
  if (window.CSS && typeof window.CSS.escape === "function") {
    return window.CSS.escape(value);
  }

  return String(value).replace(/["\\]/g, "\\$&");
}

function formatTime(totalSeconds) {
  const safeValue = Math.max(0, totalSeconds || 0);
  const minutes = Math.floor(safeValue / 60)
    .toString()
    .padStart(2, "0");
  const seconds = Math.floor(safeValue % 60)
    .toString()
    .padStart(2, "0");
  return `${minutes}:${seconds}`;
}

function shuffle(items) {
  const copy = items.slice();
  for (let index = copy.length - 1; index > 0; index -= 1) {
    const randomIndex = Math.floor(Math.random() * (index + 1));
    [copy[index], copy[randomIndex]] = [copy[randomIndex], copy[index]];
  }
  return copy;
}

function pickMessage(type) {
  const variants = MESSAGE_BANK[type] || [];
  if (!variants.length) {
    return DEFAULT_STATUS;
  }

  return variants[Math.floor(Math.random() * variants.length)];
}

