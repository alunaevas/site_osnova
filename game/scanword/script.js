function repositionCluePopup() {
  const word = getWordById(state.popupWordId);
  if (!word || !elements.cluePopup) {
    return;
  }

  const anchor = elements.board.querySelector(
    `.clue-cell[data-word-id="${escapeSelectorValue(word.id)}"]`
  );

  if (!anchor) {
    hideCluePopup();
    return;
  }

  if (isCompactViewport()) {
    const side = 10;
    const bottomOffset = 86 + safeAreaInsetBottom();
    elements.cluePopup.dataset.placement = "sheet";
    elements.cluePopup.style.left = `${side}px`;
    elements.cluePopup.style.top = "auto";
    elements.cluePopup.style.bottom = `${bottomOffset}px`;
    elements.cluePopup.style.setProperty("--arrow-x", "50%");
    return;
  }

  const margin = 12;
  const anchorRect = anchor.getBoundingClientRect();
  const popupRect = elements.cluePopup.getBoundingClientRect();
  let left = anchorRect.left + anchorRect.width / 2 - popupRect.width / 2;
  left = Math.max(margin, Math.min(left, window.innerWidth - popupRect.width - margin));

  let placement = "top";
  let top = anchorRect.top - popupRect.height - 16;

  if (top < margin) {
    placement = "bottom";
    top = anchorRect.bottom + 16;
  }

  if (top + popupRect.height > window.innerHeight - margin) {
    top = Math.max(margin, window.innerHeight - popupRect.height - margin);
  }

  const arrowX = anchorRect.left + anchorRect.width / 2 - left;
  const safeArrow = Math.max(28, Math.min(arrowX, popupRect.width - 28));

  elements.cluePopup.dataset.placement = placement;
  elements.cluePopup.style.left = `${left}px`;
  elements.cluePopup.style.top = `${top}px`;
  elements.cluePopup.style.setProperty("--arrow-x", `${safeArrow}px`);
}

function hideCluePopup() {
  if (!elements.cluePopup) {
    return;
  }

  state.popupWordId = null;
  elements.cluePopup.classList.remove("is-open");
  elements.cluePopup.setAttribute("aria-hidden", "true");
  elements.cluePopup.style.bottom = "";
}

function getWordStatus(word) {
  if (!word) {
    return { filled: false, correct: false, solved: false, revealed: false };
  }

  const revealed = state.sectionState.revealedWords.includes(word.id);
  const filled = word.cellKeys.every((key) => Boolean(state.sectionState.inputs[key]));
  const correct = word.cellKeys.every(
    (key, index) => state.sectionState.inputs[key] === word.gridAnswer[index]
  );

  return {
    filled,
    correct,
    solved: correct,
    revealed,
  };
}

function getProgressSnapshot() {
  const puzzle = state.sectionState?.puzzle;
  if (!puzzle) {
    return { total: 0, filled: 0, correct: 0, percent: 0 };
  }

  let filled = 0;
  let correct = 0;

  Object.entries(puzzle.cellsByKey).forEach(([key, cell]) => {
    const value = state.sectionState.inputs[key];
    if (value) {
      filled += 1;
    }
    if (value && value === cell.char) {
      correct += 1;
    }
  });

  return {
    total: puzzle.totalLetters,
    filled,
    correct,
    percent: puzzle.totalLetters ? Math.round((filled / puzzle.totalLetters) * 100) : 0,
  };
}

