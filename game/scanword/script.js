function generatePuzzle(section) {
  const targetWordCount = getTargetWordCount();
  const minimumWordCount = getMinimumWordCount(targetWordCount);
  let bestAttempt = null;

  for (let attemptIndex = 0; attemptIndex < MAX_PUZZLE_ATTEMPTS; attemptIndex += 1) {
    const shuffled = shuffle(section.items).slice(
      0,
      Math.min(section.items.length, targetWordCount + 4)
    );
    const attempt = buildCrosswordAttempt(shuffled, targetWordCount);

    if (!bestAttempt || scoreAttempt(attempt) > scoreAttempt(bestAttempt)) {
      bestAttempt = attempt;
    }

    if (bestAttempt.words.length >= targetWordCount && bestAttempt.intersections >= 3) {
      break;
    }
  }

  if (!bestAttempt || bestAttempt.words.length < minimumWordCount) {
    bestAttempt = buildFallbackAttempt(section.items, targetWordCount);
  }

  return finalizePuzzle(section.id, bestAttempt.words, bestAttempt.intersections, bestAttempt.layoutMode);
}

function buildCrosswordAttempt(items, targetWordCount) {
  const source = shuffle(items)
    .slice(0, Math.min(items.length, targetWordCount + 4))
    .sort((left, right) => right.gridAnswer.length - left.gridAnswer.length);

  const grid = new Map();
  const clueKeys = new Set();
  const placedWords = [];
  let intersections = 0;

  function commitWord(word) {
    const direction = DIRECTIONS[word.direction];
    placedWords.push(word);
    clueKeys.add(cellKey(word.clueRow, word.clueCol));

    for (let index = 0; index < word.gridAnswer.length; index += 1) {
      const row = word.row + direction.dr * index;
      const col = word.col + direction.dc * index;
      const key = cellKey(row, col);

      if (!grid.has(key)) {
        grid.set(key, {
          row,
          col,
          char: word.gridAnswer[index],
          directions: new Set(),
          wordIds: [],
        });
      }

      const cell = grid.get(key);
      cell.directions.add(word.direction);
      cell.wordIds.push(word.id);

      if (cell.wordIds.length === 2) {
        intersections += 1;
      }
    }
  }

  function canPlace(item, row, col, directionName) {
    const direction = DIRECTIONS[directionName];
    const length = item.gridAnswer.length;
    const clueRow = row - direction.dr;
    const clueCol = col - direction.dc;
    const clueKey = cellKey(clueRow, clueCol);
    const endRow = row + direction.dr * (length - 1);
    const endCol = col + direction.dc * (length - 1);
    const afterRow = endRow + direction.dr;
    const afterCol = endCol + direction.dc;

    if (!isInsideGrid(row, col) || !isInsideGrid(endRow, endCol) || !isInsideGrid(clueRow, clueCol)) {
      return { ok: false, intersections: 0 };
    }

    if (grid.has(clueKey) || clueKeys.has(clueKey)) {
      return { ok: false, intersections: 0 };
    }

    if (isInsideGrid(afterRow, afterCol)) {
      const afterKey = cellKey(afterRow, afterCol);
      if (grid.has(afterKey) || clueKeys.has(afterKey)) {
        return { ok: false, intersections: 0 };
      }
    }

    let crossingCount = 0;

    for (let index = 0; index < length; index += 1) {
      const currentRow = row + direction.dr * index;
      const currentCol = col + direction.dc * index;
      const currentKey = cellKey(currentRow, currentCol);

      if (!isInsideGrid(currentRow, currentCol) || clueKeys.has(currentKey)) {
        return { ok: false, intersections: 0 };
      }

      const existingCell = grid.get(currentKey);
      if (existingCell) {
        if (existingCell.char !== item.gridAnswer[index] || existingCell.directions.has(directionName)) {
          return { ok: false, intersections: 0 };
        }
        crossingCount += 1;
        continue;
      }

      const neighbors =
        directionName === "horizontal"
          ? [
              [currentRow - 1, currentCol],
              [currentRow + 1, currentCol],
            ]
          : [
              [currentRow, currentCol - 1],
              [currentRow, currentCol + 1],
            ];

      for (const [neighborRow, neighborCol] of neighbors) {
        if (!isInsideGrid(neighborRow, neighborCol)) {
          continue;
        }
        const neighborKey = cellKey(neighborRow, neighborCol);
        if (grid.has(neighborKey) || clueKeys.has(neighborKey)) {
          return { ok: false, intersections: 0 };
        }
      }
    }

    return { ok: true, intersections: crossingCount };
  }

  function collectCandidates(item) {
    const candidates = [];

    grid.forEach((existingCell) => {
      for (let index = 0; index < item.gridAnswer.length; index += 1) {
        if (existingCell.char !== item.gridAnswer[index] || existingCell.wordIds.length >= 2) {
          continue;
        }

        for (const directionName of Object.keys(DIRECTIONS)) {
          if (existingCell.directions.has(directionName)) {
            continue;
          }

          const direction = DIRECTIONS[directionName];
          const row = existingCell.row - direction.dr * index;
          const col = existingCell.col - direction.dc * index;
          const validation = canPlace(item, row, col, directionName);

          if (!validation.ok || validation.intersections === 0) {
            continue;
          }

          const distancePenalty = Math.abs(row - CENTER) + Math.abs(col - CENTER);
          candidates.push({
            row,
            col,
            direction: directionName,
            clueRow: row - direction.dr,
            clueCol: col - direction.dc,
            score: validation.intersections * 32 - distancePenalty,
          });
        }
      }
    });

    if (!candidates.length) {
      return candidates;
    }

    return candidates.sort((left, right) => right.score - left.score);
  }

  function findStandalonePlacement(item) {
    const candidates = [];
    const directions = shuffle(Object.keys(DIRECTIONS));

    for (const directionName of directions) {
      const direction = DIRECTIONS[directionName];

      for (const row of CENTERED_RANGE) {
        for (const col of CENTERED_RANGE) {
          const validation = canPlace(item, row, col, directionName);
          if (!validation.ok) {
            continue;
          }

          const clueRow = row - direction.dr;
          const clueCol = col - direction.dc;
          const distancePenalty = Math.abs(row - CENTER) + Math.abs(col - CENTER);
          candidates.push({
            row,
            col,
            direction: directionName,
            clueRow,
            clueCol,
            score: validation.intersections * 32 - distancePenalty,
          });

          if (candidates.length > 24) {
            return candidates.sort((left, right) => right.score - left.score)[0];
          }
        }
      }
    }

    if (!candidates.length) {
      return null;
    }

    return candidates.sort((left, right) => right.score - left.score)[0];
  }

  const firstWord = source.shift();
  if (!firstWord) {
    return { words: [], intersections: 0, layoutMode: "crossword" };
  }

  const preferredDirections = shuffle(["horizontal", "vertical"]);
  let firstPlacement = null;

  for (const directionName of preferredDirections) {
    const direction = DIRECTIONS[directionName];
    const row = directionName === "horizontal" ? CENTER : Math.max(1, CENTER - Math.floor(firstWord.gridAnswer.length / 2));
    const col = directionName === "horizontal" ? Math.max(1, CENTER - Math.floor(firstWord.gridAnswer.length / 2)) : CENTER;
    const validation = canPlace(firstWord, row, col, directionName);

    if (!validation.ok) {
      continue;
    }

    firstPlacement = {
      ...firstWord,
      row,
      col,
      direction: directionName,
      clueRow: row - direction.dr,
      clueCol: col - direction.dc,
    };
    break;
  }

  if (!firstPlacement) {
    return buildFallbackAttempt(items, targetWordCount);
  }

  commitWord(firstPlacement);

  for (const item of source) {
    if (placedWords.length >= targetWordCount) {
      break;
    }

    const crossingCandidate = collectCandidates(item)[0];
    const standaloneCandidate = findStandalonePlacement(item);
    const placement = crossingCandidate || standaloneCandidate;

    if (!placement) {
      continue;
    }

    commitWord({
      ...item,
      row: placement.row,
      col: placement.col,
      direction: placement.direction,
      clueRow: placement.clueRow,
      clueCol: placement.clueCol,
    });
  }

  return {
    words: placedWords,
    intersections,
    layoutMode: "crossword",
  };
}

function buildFallbackAttempt(items, targetWordCount) {
  const words = shuffle(items)
    .slice(0, Math.min(targetWordCount, items.length))
    .sort((left, right) => right.gridAnswer.length - left.gridAnswer.length)
    .slice(0, Math.min(targetWordCount, 12))
    .map((item, index) => ({
      ...item,
      row: 1 + index * 2,
      col: 2,
      direction: "horizontal",
      clueRow: 1 + index * 2,
      clueCol: 1,
    }))
    .filter((word) => word.row < GRID_SIZE - 1);

  return {
    words,
    intersections: 0,
    layoutMode: "scanline",
  };
}

function finalizePuzzle(sectionId, words, intersections, layoutMode) {
  const orderedWords = words
    .map((word) => ({ ...word }))
    .sort((left, right) => left.clueRow - right.clueRow || left.clueCol - right.clueCol);

  const cellsByKey = {};
  const clueByKey = {};
  const wordsById = {};

  orderedWords.forEach((word, index) => {
    const direction = DIRECTIONS[word.direction];
    word.number = index + 1;
    word.preview = word.preview || makeCluePreview(word.question);
    word.cellKeys = [];
    word.clueKey = cellKey(word.clueRow, word.clueCol);
    clueByKey[word.clueKey] = word.id;
    wordsById[word.id] = word;

    for (let letterIndex = 0; letterIndex < word.gridAnswer.length; letterIndex += 1) {
      const row = word.row + direction.dr * letterIndex;
      const col = word.col + direction.dc * letterIndex;
      const key = cellKey(row, col);

      if (!cellsByKey[key]) {
        cellsByKey[key] = {
          row,
          col,
          char: word.gridAnswer[letterIndex],
          wordIds: [],
          startNumbers: [],
        };
      }

      cellsByKey[key].wordIds.push(word.id);
      if (letterIndex === 0) {
        cellsByKey[key].startNumbers.push(word.number);
      }
      word.cellKeys.push(key);
    }
  });

  return rebuildDerivedData({
    id: `${sectionId}-${Date.now()}`,
    sectionId,
    layoutMode,
    intersections,
    words: orderedWords,
    wordsById,
    cellsByKey,
    clueByKey,
    bounds: calculateBounds(orderedWords),
  });
}

function rebuildDerivedData(puzzle) {
  const wordsById = {};
  const cellsByKey = puzzle.cellsByKey || {};
  const clueByKey = puzzle.clueByKey || {};
  let totalLetters = 0;

  for (const key of Object.keys(cellsByKey)) {
    totalLetters += 1;
  }

  puzzle.words.forEach((word) => {
    wordsById[word.id] = word;
    if (!word.clueKey) {
      word.clueKey = cellKey(word.clueRow, word.clueCol);
    }
  });

  return {
    ...puzzle,
    wordsById,
    cellsByKey,
    clueByKey,
    bounds: puzzle.bounds || calculateBounds(puzzle.words),
    totalLetters,
  };
}

function calculateBounds(words) {
  let minRow = GRID_SIZE;
  let minCol = GRID_SIZE;
  let maxRow = 0;
  let maxCol = 0;

  words.forEach((word) => {
    minRow = Math.min(minRow, word.clueRow);
    minCol = Math.min(minCol, word.clueCol);
    maxRow = Math.max(maxRow, word.clueRow);
    maxCol = Math.max(maxCol, word.clueCol);

    const direction = DIRECTIONS[word.direction];
    for (let index = 0; index < word.gridAnswer.length; index += 1) {
      const row = word.row + direction.dr * index;
      const col = word.col + direction.dc * index;
      minRow = Math.min(minRow, row);
      minCol = Math.min(minCol, col);
      maxRow = Math.max(maxRow, row);
      maxCol = Math.max(maxCol, col);
    }
  });

  return {
    minRow: Math.max(0, minRow - 1),
    minCol: Math.max(0, minCol - 1),
    maxRow: Math.min(GRID_SIZE - 1, maxRow + 1),
    maxCol: Math.min(GRID_SIZE - 1, maxCol + 1),
  };
}

function scoreAttempt(attempt) {
  if (!attempt.words.length) {
    return -Infinity;
  }

  const bounds = calculateBounds(attempt.words);
  const area = (bounds.maxRow - bounds.minRow + 1) * (bounds.maxCol - bounds.minCol + 1);
  return attempt.words.length * 120 + attempt.intersections * 10 - area * 0.22;
}

function renderPuzzle() {
  const puzzle = state.sectionState?.puzzle;
  if (!puzzle || !puzzle.words.length) {
    renderEmptyState();
    return;
  }

  const columnCount = puzzle.bounds.maxCol - puzzle.bounds.minCol + 1;
  const rows = [];

  for (let row = puzzle.bounds.minRow; row <= puzzle.bounds.maxRow; row += 1) {
    for (let col = puzzle.bounds.minCol; col <= puzzle.bounds.maxCol; col += 1) {
      const key = cellKey(row, col);
      const clueWordId = puzzle.clueByKey[key];
      const cell = puzzle.cellsByKey[key];

      if (clueWordId) {
        const word = puzzle.wordsById[clueWordId];
        const direction = DIRECTIONS[word.direction];

        rows.push(`
          <button
            class="clue-cell"
            type="button"
            data-word-id="${escapeHtml(word.id)}"
            title="${escapeHtml(word.question)}"
            aria-label="Подсказка ${word.number}, ${direction.label}"
          >
            <span class="clue-cell__top">
              <span>${String(word.number).padStart(2, "0")}</span>
              <span>${direction.arrow}</span>
            </span>
            <span class="clue-cell__preview">${escapeHtml(word.preview)}</span>
          </button>
        `);
        continue;
      }

      if (cell) {
        const marker = cell.startNumbers.length
          ? `<span class="letter-shell__tag">${escapeHtml(cell.startNumbers.join("/"))}</span>`
          : "";

        rows.push(`
          <label class="letter-shell" data-key="${escapeHtml(key)}">
            ${marker}
            <input
              class="board-cell"
              type="text"
              inputmode="text"
              maxlength="1"
              autocapitalize="characters"
              autocomplete="off"
              autocorrect="off"
              spellcheck="false"
              aria-label="Буква"
              data-key="${escapeHtml(key)}"
            >
          </label>
        `);
        continue;
      }

      rows.push('<div class="void-cell" aria-hidden="true"></div>');
    }
  }

  elements.board.style.gridTemplateColumns = `repeat(${columnCount}, var(--fit-cell-size, var(--cell-size)))`;
  elements.board.innerHTML = rows.join("");
  renderClueList();
  window.requestAnimationFrame(applyBoardSizing);
}

